//! Self-contained HTML reproducibility report — full provenance of one analysis in a
//! single file (inline CSS, no external assets): the sample + instrument, compensation
//! matrix and its source, display transforms, the gating strategy, per-population
//! statistics, the flowCore-validation note, and the gate JSON for machine re-import.
//! Pure generation (`html`); the GUI gathers `ReportData` from its state.

use crate::popstats::PopulationStatsTable;

pub struct ChannelRow {
    pub name: String,
    pub stain: String,
    pub transform: String,
}

pub struct GateRow {
    pub depth: usize,
    pub name: String,
    pub parent: String,
    pub channels: String,
    pub shape: String,
}

pub struct ReportData {
    pub version: String,
    pub generated: String,
    pub file_name: String,
    pub n_events: usize,
    pub cytometer: String,
    pub acq_date: String,
    pub channels: Vec<ChannelRow>,
    pub compensated: bool,
    pub spill_channels: Vec<String>, // empty ⇒ no compensation matrix
    pub spill_rows: Vec<Vec<f64>>,
    pub spill_source: String,
    pub gates: Vec<GateRow>,
    pub stats: Option<PopulationStatsTable>,
    pub gates_json: String,
}

/// Escape text for safe HTML embedding (data-derived strings: names, stains, gates…).
fn esc(s: &str) -> String {
    s.replace('&', "&amp;").replace('<', "&lt;").replace('>', "&gt;").replace('"', "&quot;")
}

/// Current UTC time as "YYYY-MM-DD HH:MM:SS UTC" (dependency-free; civil-from-days).
pub fn now_utc() -> String {
    let secs = std::time::SystemTime::now()
        .duration_since(std::time::UNIX_EPOCH)
        .map(|d| d.as_secs())
        .unwrap_or(0) as i64;
    civil(secs)
}

fn civil(secs: i64) -> String {
    let days = secs.div_euclid(86400);
    let tod = secs.rem_euclid(86400);
    let (h, mi, s) = (tod / 3600, (tod % 3600) / 60, tod % 60);
    // Howard Hinnant's civil_from_days.
    let z = days + 719468;
    let era = if z >= 0 { z } else { z - 146096 } / 146097;
    let doe = z - era * 146097;
    let yoe = (doe - doe / 1460 + doe / 36524 - doe / 146096) / 365;
    let y = yoe + era * 400;
    let doy = doe - (365 * yoe + yoe / 4 - yoe / 100);
    let mp = (5 * doy + 2) / 153;
    let d = doy - (153 * mp + 2) / 5 + 1;
    let m = if mp < 10 { mp + 3 } else { mp - 9 };
    let y = if m <= 2 { y + 1 } else { y };
    format!("{:04}-{:02}-{:02} {:02}:{:02}:{:02} UTC", y, m, d, h, mi, s)
}

fn fmt_num(v: f64) -> String {
    if v.is_finite() { format!("{:.2}", v) } else { "—".to_string() }
}

/// Build the complete, self-contained HTML document.
pub fn html(r: &ReportData) -> String {
    let mut h = String::new();
    h.push_str(&format!(
        "<!doctype html>\n<html lang=\"en\"><head><meta charset=\"utf-8\">\n\
         <meta name=\"viewport\" content=\"width=device-width, initial-scale=1\">\n\
         <title>flowcyto report — {}</title>\n<style>{}</style>\n</head><body>\n\
         <div class=\"wrap\">\n",
        esc(&r.file_name), CSS
    ));
    h.push_str(&format!(
        "<h1>flowcyto analysis report</h1>\n\
         <p class=\"meta\">Generated by flowcyto v{} · {}</p>\n",
        esc(&r.version), esc(&r.generated)
    ));

    // Sample.
    h.push_str("<h2>Sample</h2>\n<table class=\"kv\">\n");
    for (k, v) in [
        ("File", r.file_name.clone()),
        ("Events", r.n_events.to_string()),
        ("Cytometer", r.cytometer.clone()),
        ("Acquired", r.acq_date.clone()),
    ] {
        h.push_str(&format!("<tr><th>{}</th><td>{}</td></tr>\n", k, esc(&v)));
    }
    h.push_str("</table>\n");

    // Channels & transforms.
    h.push_str("<h2>Channels &amp; display transforms</h2>\n\
                <table><thead><tr><th>Channel</th><th>Stain</th><th>Transform</th></tr></thead><tbody>\n");
    for c in &r.channels {
        h.push_str(&format!("<tr><td>{}</td><td>{}</td><td>{}</td></tr>\n",
            esc(&c.name), esc(&c.stain), esc(&c.transform)));
    }
    h.push_str("</tbody></table>\n");

    // Compensation.
    h.push_str("<h2>Compensation</h2>\n");
    if !r.compensated {
        h.push_str("<p class=\"note\">Not applied — analysis is on raw (uncompensated) data.</p>\n");
    } else if r.spill_channels.is_empty() {
        h.push_str("<p class=\"note\">Compensation enabled, but no spillover matrix was present.</p>\n");
    } else {
        h.push_str(&format!("<p class=\"note\">Source: {}</p>\n", esc(&r.spill_source)));
        h.push_str("<table class=\"matrix\"><thead><tr><th>spill →</th>");
        for c in &r.spill_channels {
            h.push_str(&format!("<th>{}</th>", esc(c)));
        }
        h.push_str("</tr></thead><tbody>\n");
        for (i, row) in r.spill_rows.iter().enumerate() {
            h.push_str(&format!("<tr><th>{}</th>", esc(r.spill_channels.get(i).map(|s| s.as_str()).unwrap_or(""))));
            for (j, v) in row.iter().enumerate() {
                let cls = if i == j { " class=\"diag\"" } else if v.abs() > 1e-9 { " class=\"off\"" } else { "" };
                h.push_str(&format!("<td{}>{:.4}</td>", cls, v));
            }
            h.push_str("</tr>\n");
        }
        h.push_str("</tbody></table>\n");
    }

    // Gating strategy.
    h.push_str("<h2>Gating strategy</h2>\n");
    if r.gates.is_empty() {
        h.push_str("<p class=\"note\">No gates defined.</p>\n");
    } else {
        h.push_str("<table><thead><tr><th>Population</th><th>Parent</th><th>Channels</th><th>Definition</th></tr></thead><tbody>\n");
        for g in &r.gates {
            let indent = "&nbsp;&nbsp;".repeat(g.depth);
            h.push_str(&format!(
                "<tr><td>{}{}</td><td>{}</td><td>{}</td><td class=\"mono\">{}</td></tr>\n",
                indent, esc(&g.name), esc(&g.parent), esc(&g.channels), esc(&g.shape)
            ));
        }
        h.push_str("</tbody></table>\n");
    }

    // Population statistics.
    h.push_str("<h2>Population statistics</h2>\n");
    if let Some(t) = &r.stats {
        h.push_str("<div class=\"scroll\"><table><thead><tr>\
                    <th>Population</th><th>Count</th><th>%Parent</th><th>%Total</th>");
        for ch in &t.channels {
            h.push_str(&format!("<th>{}<br><span class=\"sub\">MFI</span></th>", esc(ch)));
        }
        h.push_str("</tr></thead><tbody>\n");
        for row in &t.rows {
            let indent = "&nbsp;&nbsp;".repeat(row.depth);
            h.push_str(&format!(
                "<tr><td>{}{}</td><td>{}</td><td>{}</td><td>{}</td>",
                indent, esc(&row.name), row.count, fmt_num(row.pct_parent), fmt_num(row.pct_total)
            ));
            for m in &row.medians {
                h.push_str(&format!("<td>{}</td>", fmt_num(*m)));
            }
            h.push_str("</tr>\n");
        }
        h.push_str("</tbody></table></div>\n");
    } else {
        h.push_str("<p class=\"note\">No statistics computed.</p>\n");
    }

    // Reproducibility.
    h.push_str("<h2>Reproducibility</h2>\n\
        <p>The numeric layers (parsing, compensation, asinh, logicle) are validated \
        against R/flowCore — run <code>flowcyto selftest</code> to reproduce the benchmark. \
        The gating below can be re-imported into flowcyto (Gates → Load).</p>\n");
    h.push_str(&format!(
        "<details><summary>Gate definitions (JSON)</summary><pre class=\"json\">{}</pre></details>\n",
        esc(&r.gates_json)
    ));

    h.push_str("</div>\n</body></html>\n");
    h
}

