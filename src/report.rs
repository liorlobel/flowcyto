#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn civil_from_known_epoch() {
        // 1700000000 = 2023-11-14 22:13:20 UTC.
        assert_eq!(civil(1_700_000_000), "2023-11-14 22:13:20 UTC");
        assert_eq!(civil(0), "1970-01-01 00:00:00 UTC");
    }

    #[test]
    fn html_has_sections_and_escapes() {
        let r = ReportData {
            version: "9.9.9".into(), generated: "2026-01-01 00:00:00 UTC".into(),
            file_name: "a<b>.fcs".into(), n_events: 1234, cytometer: "LSRII".into(),
            acq_date: "01-JAN-2026".into(),
            channels: vec![ChannelRow { name: "FITC-A".into(), stain: "CD3".into(), transform: "Logicle".into() }],
            compensated: true,
            spill_channels: vec!["FITC-A".into(), "PE-A".into()],
            spill_rows: vec![vec![1.0, 0.08], vec![0.03, 1.0]],
            spill_source: "embedded $SPILLOVER".into(),
            gates: vec![GateRow { depth: 1, name: "CD3+".into(), parent: "Live".into(),
                                  channels: "FITC-A (Logicle)".into(), shape: "interval [1.2, 4.5]".into() }],
            stats: None, gates_json: "[{\"id\":1}]".into(),
        };
        let h = html(&r);
        assert!(h.contains("flowcyto analysis report"));
        assert!(h.contains("Compensation") && h.contains("Gating strategy") && h.contains("flowcyto selftest"));
        assert!(h.contains("a&lt;b&gt;.fcs"), "file name must be HTML-escaped");
        assert!(!h.contains("a<b>.fcs"), "raw unescaped name must not appear");
        assert!(h.contains("CD3+") && h.contains("1234"));
    }
}
